// Equality (==)
console.log(1 == 1);

console.log("hello" == "hello");

console.log("1" == 1);

console.log(0 == false);
console.log("\n");

// Inequality (!=)
console.log(1 != 1);

console.log("hello" != "hello");

console.log("1" != 1);

console.log(0 != false);
console.log("\n");

// Strict equality (===)
console.log(1 === 1);

console.log("hello" === "hello");

console.log("1" === 1);

console.log(0 === false);
console.log("\n");

// Strict inequality (!==)
console.log(1 !== 1);

console.log("hello" !== "hello");

console.log("1" !== 1);

console.log(0 !== false);
console.log("\n");

// Greater than (>)
console.log(5 > 3);

console.log(3 > 3);

console.log(3n > 5);

console.log("ab" > "aa");
console.log("\n");

// Greater than or equal (>=)
console.log(5 >= 3);

console.log(3 >= 3);

console.log(3n >= 5);

console.log("ab" >= "aa");
console.log("\n");

// Less than (<)
console.log(5 < 3);

console.log(3 < 3);

console.log(3n < 5);

console.log("aa" < "ab");
console.log("\n");

// Less than or equal (<=)
console.log(5 <= 3);

console.log(3 <= 3);

console.log(3n <= 5);

console.log("aa" <= "ab");
console.log("\n");
