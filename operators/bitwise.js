// Bitwise AND (&)
const a = 5;
const b = 3;

console.log(a & b);
console.log("\n");

// Bitwise OR (|)
const a1 = 5;
const b1 = 3;

console.log(a1 | b1);
console.log("\n");

//Bitwise XOR (^)
const a2 = 5;
const b2 = 3;

console.log(a2 ^ b2);
console.log("\n");

//Bitwise NOT (~)
const a3 = 5;
const b3 = -3;

console.log(~a3);

console.log(~b3);
console.log("\n");

// Left shift (<<)
const a4 = 5;
const b4 = 2;
console.log(a4 << b4);
console.log("\n");

//Right shift (>>)
const a5 = 5;
const b5 = 2;
const c = -5;

console.log(a5 >> b5);

console.log(c >> b5);
console.log("\n");

// Unsigned right shift (>>>)
const a6 = 5;
const b6 = 2;
const c1 = -5;

console.log(a6 >>> b6);

console.log(c1 >>> b6);
console.log("\n");
