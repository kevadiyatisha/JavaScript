// Remainder (%)
console.log(13 % 5);

console.log(-13 % 5);

console.log(4 % 2);

console.log(-4 % 2);
console.log("\n");

// Increment (++)
let x = 3;
const y = x++;

console.log(`x:${x}, y:${y}`);

let a = 3;
const b = ++a;

console.log(`a:${a}, b:${b}`);
console.log("\n");

// Decrement (--)
let x1 = 3;
const y1 = x1--;

console.log(`x:${x1}, y:${y1}`);

let a1 = 3;
const b1 = --a;

console.log(`a:${a1}, b:${b1}`);
console.log("\n");

// Unary negation (-)
const x2 = 4;
const y2 = -x;

console.log(y2);

const a2 = "4";
const b2 = -a;

console.log(b2);
console.log("\n");

// Unary plus (+)
const x3 = 1;
const y3 = -1;

console.log(+x3);

console.log(+y3);

console.log(+"");

console.log(+true);

console.log(+false);

console.log(+"hello");
console.log("\n");

// Exponentiation (**)
console.log(3 ** 4);

console.log(10 ** -2);

console.log(2 ** (3 ** 2));

console.log((2 ** 3) ** 2);
console.log("\n");
