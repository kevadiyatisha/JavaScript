//If Statement
const num = 5;

if (num > 0) {
  console.log("The number is positive.");
}
console.log("\n");

//If-Else Statement
let num1 = -10;

if (num1 > 0) console.log("The number is positive.");
else console.log("The number is negative");
console.log("\n");

//switch statement
let num3 = 5;

switch (num3) {
  case 0:
    console.log("num is zero.");
    break;
  case 1:
    console.log("num is one.");
    break;
  case 2:
    console.log("num is two.");
    break;
  case 3:
    console.log("num is three.");
    break;
  default:
    console.log("number is greater than 2.");
}
